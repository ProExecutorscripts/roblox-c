LIBS = ["malloc", "free", "realloc", "calloc", "memset", "memcpy", "memmove", "memcmp", "memchr", "printf"]

def gen(code):
    header = ""
    for lib in LIBS:
        if lib in code:
            header += lib+" = C."+lib+"\n"
    return header
