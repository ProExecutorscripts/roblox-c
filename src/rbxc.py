def main():
    # read args
    args = sys.argv[1:]
    flags = []
    inputf = None
    outputf = None
    check = True

    lookForOutput = False
    skip = False
    hardcore = False
    
    if isconfig("lclang"):
        Config.set_library_file(isconfig("lclang"))
        
    flags = []
    
    for i, arg in enumerate(args):
        if arg.startswith("-W"):
            flags.append(arg)
        elif arg == "-o":
            lookForOutput = True
        elif arg == "-c":
            check = False
        elif arg == "-h":
            hardcore = True
        elif arg == "-p":
            try:
                Config.set_library_file(args[i+1])
                config("lclang", args[i+1])
                skip = True
            except:
                config("lclang", None)
                error("no path specified")
                sys.exit(1)
        elif arg == "-v":
            version()
        elif arg == "-vd":
            print(VERSION)
            sys.exit(0)
        elif arg == "-u":
            usage()
        elif arg.startswith("-"):
            flags.append(arg)
        elif (inputf is None) and "-s" not in args:
            inputf = arg
        elif lookForOutput:
            outputf = arg
            lookForOutput = False
        elif skip:
            skip = False
        else:
            error("too many arguments")
            sys.exit(1)
    
    if (inputf is None) and "-s" not in args:
        usage()
        sys.exit(1)
    
    if outputf is None:
        error("no output file specified")
        sys.exit(1)
        
    if "-s" in args:
        print(crun.cruntime)
        sys.exit(0)
    
    isC = None
    if inputf.endswith(".c"):
        isC = True
    elif inputf.endswith(".cpp") or inputf.endswith(".cxx") or inputf.endswith(".cc") or inputf.endswith(".C"):
        isC = False
    else:
        error("file must end with '.c', '.cpp', '.cxx', '.cc', or '.C'")
        sys.exit(1)
        
    parsed = get_ast(inputf, isC, check, flags)
    if not check:
        print_ast(parsed)
    Engine = NodeVisitor(isC)
    Engine.visit(parsed)
    Enginecode = Engine.gen()
    with open(outputf, "w") as f:
        code = (HEADER + gen(Enginecode) + Enginecode)
        if hardcore:
            code = "xpcall(function() -- hardcore mode\n" + code + "\nend, function(err) -- hardcore mode\n\terror('Segmenation fault: 11') -- hardcore mode\nend) -- hardcore mode"
            
        f.write(code)
    
if __name__ == "__main__":
    main()